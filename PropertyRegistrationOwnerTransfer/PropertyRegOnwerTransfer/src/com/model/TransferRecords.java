package com.model;

public class TransferRecords {
	String trid, pid, preoname, preoid, newoname, newoid, addrs, reg, prop, tradate;

	public String getTradate() {
		return tradate;
	}

	public void setTradate(String tradate) {
		this.tradate = tradate;
	}

	public String getProp() {
		return prop;
	}

	public void setProp(String prop) {
		this.prop = prop;
	}

	public String getTrid() {
		return trid;
	}

	public void setTrid(String trid) {
		this.trid = trid;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getPreoname() {
		return preoname;
	}

	public void setPreoname(String preoname) {
		this.preoname = preoname;
	}

	public String getPreoid() {
		return preoid;
	}

	public void setPreoid(String preoid) {
		this.preoid = preoid;
	}

	public String getNewoname() {
		return newoname;
	}

	public void setNewoname(String newoname) {
		this.newoname = newoname;
	}

	public String getNewoid() {
		return newoid;
	}

	public void setNewoid(String newoid) {
		this.newoid = newoid;
	}

	public String getAddrs() {
		return addrs;
	}

	public void setAddrs(String addrs) {
		this.addrs = addrs;
	}

	public String getReg() {
		return reg;
	}

	public void setReg(String reg) {
		this.reg = reg;
	}

}
